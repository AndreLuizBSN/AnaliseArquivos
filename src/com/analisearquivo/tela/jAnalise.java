    private void btnAbrirArq1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAbrirArq1ActionPerformed
        // TODO add your handling code here:
        JFileChooser jFileChooser = new JFileChooser();
        jFileChooser.showDialog(null, null);
        
        File file = jFileChooser.getSelectedFile();
        
        if(file.exists()){
        	this.file1 = new Analise(file.getParent()+"\\"+file.getName());
        	lbDir1.setText(file1.getDiretorio());
        	lbDt1.setText(file1.getDataModificacao());
        	lbTam1.setText(String.valueOf(file1.getTamanho())+" Bytes");
        	chEscrever1.setSelected(file1.isEscrever());
        	chLer1.setSelected(file1.isLer());
        	chExecutar1.setSelected(file1.isExecutar());
        }
        
        txtArq1.setText(file.getParent()+"\\"+file.getName());
        
    }//GEN-LAST:event_btnAbrirArq1ActionPerformed

