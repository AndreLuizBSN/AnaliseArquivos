    private void btnCompararActionPerformed(java.awt.event.ActionEvent evt) {
    	
    	try{
    		ComparaArquivo c = new ComparaArquivo(this.file1, this.file2);
    		lbInfo1.setText(c.getComparaGeral());
    		lbInfo2.setText(c.getComparaTamanhos());
    		lbInfo3.setText(c.getComparaDatas());
    	}catch(Exception e){
    		lbInfo1.setText("É necessário que seja selecionado os 2 arquivos para comparação!");
    	}
    	
    }
    
